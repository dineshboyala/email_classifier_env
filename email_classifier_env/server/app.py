"""
FastAPI application for the Email Classifier Env Environment.

This module creates an HTTP server that exposes the EmailClassifierEnvironment
over HTTP and WebSocket endpoints, compatible with EnvClient.

Endpoints:
    - POST /reset: Reset the environment
    - POST /step: Execute an action
    - GET /state: Get current environment state
    - GET /schema: Get action/observation schemas
    - WS /ws: WebSocket endpoint for persistent sessions

Usage:
    # Development (with auto-reload):
    uvicorn server.app:app --reload --host 0.0.0.0 --port 8000

    # Production:
    uvicorn server.app:app --host 0.0.0.0 --port 8000 --workers 4

    # Or run directly:
    python -m server.app
"""

from openenv.core.env_server.http_server import create_app

try:
    from ..models import EmailAction, EmailObservation
    from .email_classifier_env_environment import EmailClassifierEnvironment
except ModuleNotFoundError:
    from models import EmailAction, EmailObservation
    from server.email_classifier_env_environment import EmailClassifierEnvironment


app = create_app(
    EmailClassifierEnvironment,
    EmailAction,
    EmailObservation,
    env_name="email_classifier_env",
    max_concurrent_envs=1,
)


def main(host: str = "0.0.0.0", port: int = 8000):
    import uvicorn
    uvicorn.run(app, host=host, port=port)


if __name__ == "__main__":
    import argparse

    parser = argparse.ArgumentParser()
    parser.add_argument("--port", type=int, default=8000)
    args = parser.parse_args()
    main(port=args.port)










    